export function getVermoji(changelogPath: string) {
  const changelog = fs.readFileSync(changelogPath, 'utf8');

  while (true) {
    const vermoji = VERMOJIS[Math.round(Math.random() * (VERMOJIS.length - 1))];
    if (!changelog.includes(vermoji)) {
      return vermoji;
    }
  }
}
